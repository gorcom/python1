# Задача-2:
# Даны два произвольные списка.
# Удалите из первого списка элементы, присутствующие во втором списке.

list1 = ["яблоко", "банан", "киви", "арбуз", "яблоко-банан", "яблоко", "яблоко", "яблоко", "яблоко", "яблоко", "яблоко"]
list2 = ["яблоко", "банан"]

for x in list2:
    while list1.count(x) > 0:
        list1.remove(x)
