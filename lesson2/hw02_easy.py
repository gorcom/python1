# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

fruits = ["яблоко", "банан", "киви", "арбуз", "яблоко-банан"]
fruits_len = []
for fruit in fruits:
    fruits_len.append(len(fruit))
for num, fruit in enumerate(fruits, 1):
    print(f"{num}. {fruit:>{(max(fruits_len))}}")

